vers = '00.00.01'

class LedBrd():
    numLedBrds = 0
    
    #Used for blinking Leds at 500 ms.
    currBlinkLeds = []
    
    #Current data output to the LEDs
    currLedData = []
    
    def add_card(self):
        self.numLedBrds += 1
        self.currBlinkLeds.append(0)
        self.currLedData.append(0)
